package edu.danilotc.javaoneforall.introduction.arrays;

public class Array01 {
    public static void main(String[] args) {
        // definition
        int[] ages = new int[3];
        // inicialization
        ages[0] = 12;
        ages[1] = 13;
        ages[2] = 15;
        // access and use
        System.out.println(ages[0]);
        System.out.println(ages[1]);
        System.out.println(ages[2]);
    }
}
